package com.example.myapplication;

import androidx.annotation.NonNull;

public class Type {
    private int id;
    private String name;
    public Type(){}

    public Type( String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @NonNull
    @Override
    public String toString() {
        return getName();
    }
}
